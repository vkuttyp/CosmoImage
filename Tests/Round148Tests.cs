/// <summary>
/// Round 148 — multi-part EXR (first part only). Multi-part files
/// have version bit 12 set, multiple null-terminated headers
/// followed by an extra null, per-part offset tables, and a 4-byte
/// part-number prefix on every chunk. We expose part 0 only;
/// non-image part 0 (deeps) returns null so the caller can fall back.
/// </summary>
public class Round148Tests
{
    [Fact]
    public void Pure_MultiPartScanlineExr_FirstPartDecodes()
    {
        // 4×2 RGB HALF, single-part-format-as-multi-part (one image part).
        int w = 4, h = 2;
        var rgb = new ushort[w * h * 3];
        for (int i = 0; i < rgb.Length; i++) rgb[i] = (ushort)((0x3C00 + i) & 0xFFFF);

        var exr = BuildMultiPartScanlineExr(rgb, w, h, partType: "scanlineimage");
        var img = PureExrDecoder.TryDecode(exr);
        Assert.NotNull(img);
        Assert.Equal(w, img!.Width);
        Assert.Equal(h, img.Height);
        Assert.Equal(3, img.Bands);

        var got = ReadFloats(img.PixelsLazy!.Value);
        for (int i = 0; i < rgb.Length; i++)
        {
            float expected = (float)BitConverter.UInt16BitsToHalf(rgb[i]);
            Assert.Equal(expected, got[i]);
        }
    }

